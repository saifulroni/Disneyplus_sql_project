CREATE TABLE disneyplus (
	show_id 	VARCHAR (10),
	type 		VARCHAR (10),
	title		VARCHAR (200),
	director 	VARCHAR (200),
	casts		VARCHAR (1000),
	country		VARCHAR (150),
	date_added 	VARCHAR (50),
	release_year INT,
	rating		VARCHAR (20),
	duration	VARCHAR (20),
	listed_in	VARCHAR (100),
	description VARCHAR (250)
);


